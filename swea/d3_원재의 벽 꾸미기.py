def solution(n, a, b, idx):
    answer = 1e9

    for i in range(1, n + 1):
        j = 1
        while True:
            if i * j > n:
                break
            value = a * abs(j - i) + b * abs(n - j * i)
            if value < answer:
                answer = value
            j += 1

    print(f'#{idx} {answer}')

T = int(input())

for i in range(T):
    n, a, b = map(int, input().split())
    solution(n, a, b, i + 1)

